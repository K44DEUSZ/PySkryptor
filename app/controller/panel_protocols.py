# app/controller/panel_protocols.py
from __future__ import annotations

from collections.abc import Sequence
from typing import Any, Protocol, TypeAlias, runtime_checkable

from app.model.core.config.profiles import RuntimeProfiles
from app.model.core.domain.entities import SettingsSnapshot, TranscriptionSessionRequest
from app.model.core.domain.results import SourceExpansionResult
from app.model.core.domain.state import AppRuntimeState
from app.model.download.domain import SourceAccessInterventionResolution

ErrorParams: TypeAlias = dict[str, Any]
ProbeRow: TypeAlias = dict[str, Any]
ProbeBatch: TypeAlias = list[ProbeRow]
SourcePayload: TypeAlias = dict[str, Any]
SourceEntry: TypeAlias = str | SourcePayload
ProbeMeta: TypeAlias = dict[str, Any]
SettingsPayload: TypeAlias = dict[str, Any]
WorkerRef: TypeAlias = Any
SpectrumValues: TypeAlias = Sequence[float]


@runtime_checkable
class FilesCoordinatorProtocol(Protocol):
    """Controller-facing contract for the files panel workflow."""
    def is_probe_running(self) -> bool: ...
    def is_transcribing(self) -> bool: ...
    def is_expanding(self) -> bool: ...
    def is_options_save_running(self) -> bool: ...
    def start_probe(self, entries: list[SourcePayload]) -> WorkerRef | None: ...
    def cancel_probe(self) -> None: ...
    def start_transcription(
        self,
        *,
        entries: list[SourceEntry],
        session_request: TranscriptionSessionRequest,
    ) -> WorkerRef | None: ...
    def cancel_transcription(self) -> None: ...
    def resolve_conflict(self, action: str, new_stem: str = "") -> None: ...
    def save_quick_options(self, payload: SettingsPayload) -> WorkerRef | None: ...
    def expand_manual_input(self, raw: str) -> WorkerRef | None: ...
    def expand_local_paths(self, paths: list[str], origin_kind: str) -> WorkerRef | None: ...
    def cancel_expansion(self) -> None: ...
    def resolve_access_intervention(
        self,
        source_key: str,
        resolution: SourceAccessInterventionResolution,
    ) -> None: ...


class FilesPanelViewProtocol(Protocol):
    """View callbacks exposed by the files panel."""

    def bind_coordinator(self, coordinator: FilesCoordinatorProtocol) -> None: ...

    def on_runtime_state_changed(self, state: AppRuntimeState) -> None: ...
    def on_meta_rows_ready(self, batch: ProbeBatch) -> None: ...
    def on_meta_item_error(self, key: str, err_key: str, params: ErrorParams) -> None: ...
    def on_meta_finished(self) -> None: ...
    def on_global_progress(self, value: int) -> None: ...
    def on_item_status(self, key: str, status: str) -> None: ...
    def on_item_progress(self, key: str, pct: int) -> None: ...
    def on_item_path_update(self, old_key: str, new_key: str) -> None: ...
    def on_transcript_ready(self, key: str, transcript_path: str) -> None: ...
    def on_item_error(self, key: str, err_key: str, params: ErrorParams) -> None: ...
    def on_item_output_dir(self, key: str, out_dir: str) -> None: ...
    def on_conflict_check(self, stem: str, existing_dir: str) -> None: ...
    def on_session_done(
        self,
        session_dir: str,
        processed_any: bool,
        had_errors: bool,
        was_cancelled: bool,
    ) -> None: ...
    def on_transcribe_finished(self) -> None: ...
    def refresh_defaults_from_settings(self) -> None: ...
    def on_quick_options_save_error(self, key: str, params: ErrorParams) -> None: ...
    def on_expansion_busy_changed(self, busy: bool) -> None: ...
    def on_expansion_status_changed(self, key: str, params: ErrorParams) -> None: ...
    def on_expansion_ready(self, result: SourceExpansionResult) -> None: ...
    def on_expansion_error(self, key: str, params: ErrorParams) -> None: ...
    def on_access_intervention_required(self, source_key: str, params: ErrorParams) -> None: ...


@runtime_checkable
class LiveCoordinatorProtocol(Protocol):
    """Controller-facing contract for the live transcription panel."""
    def is_running(self) -> bool: ...
    def is_options_save_running(self) -> bool: ...
    def save_transcript(
        self,
        *,
        target_path: str,
        source_text: str,
        target_text: str,
        write_source_companion: bool,
    ) -> list[str]: ...
    def start_session(
        self,
        *,
        device_name: str = "",
        source_language: str = "",
        target_language: str = "",
        translate_enabled: bool = False,
        profile: str = RuntimeProfiles.LIVE_DEFAULT_PROFILE,
        runtime_profile: dict[str, Any] | None = None,
        output_mode: str = RuntimeProfiles.LIVE_OUTPUT_MODE_CUMULATIVE,
    ) -> WorkerRef | None: ...
    def cancel(self) -> None: ...
    def stop(self) -> None: ...
    def pause(self) -> None: ...
    def resume(self) -> None: ...
    def list_input_devices(self) -> list[str]: ...
    def save_quick_options(self, payload: SettingsPayload) -> WorkerRef | None: ...


